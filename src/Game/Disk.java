package Game;

public class Disk {
	
	private int size;
	
	public Disk(int size) {
		this.size = size;
	}
	
	/***
	 * Método para obter o tamanho do disco.
	 * @return tamanho do disco.
	 */
	public int getSize() {
		return size;
	}
	
	/***
	 * Método para introduzir um tamanho ao disco.
	 * @param size tamanho do disco.
	 */
	public void setSize(int size) {
		this.size = size;
	}
	
	
	
	
}
